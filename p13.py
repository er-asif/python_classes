n1 = int(input("Enter 1st Number :"))
n2 = int(input("Enter 2nd Number :"))

def add(num1,num2):
	sum = num1+num2
	print("Sum =",sum)
def sub(num1,num2):
	sub = num1-num2
	print("Sub =",sub)
def mult(num1,num2):
	mult = num1*num2
	print("Mult =",mult)
def div(num1,num2):
	div = num1/num2
	print("Div =",div)
add(n1,n2)
sub(n1,n2)
